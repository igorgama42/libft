char	*ft_strchr(const char *s, int c)
{
	char	temp_ch;
	int		i;

	temp_ch = c;
	i = 0;
	while (*(s + i) && (*(s + i) != temp_ch))
		i++;
	if (*(s + i) == temp_ch)
		return ((char *) s + i);
	return (0);
}
